<script>
$(document).ready(function(){

        $('.fancybox').fancybox();
        $('#selector').change(function(){
            $('.all').hide();
            $('.' + $(this).val()).show();
        });
						   					   
});
$(document).ready(function() {
				$('.menu').fancySelect().on('change', function() {
					newSection = $('.' + $(this).val())

					if (newSection.hasClass('current')) {
						return;
					}

					$('section').removeClass('current');
					newSection.addClass('current');

					$('section:not(.current)').fadeOut(300, function() {
						newSection.fadeIn(300);
					});
				});
				
				
/**Porfolio Sorting when user come from services page to porfolio page**/					
var path = window.location.hash;
if(path=="#website"){
$(".all").hide();		
$(".website").show();
}
else if(path=="#ecommerce"){
$(".all").hide();	
$(".ecommerce").show();
var ecom = $( "div.trigger" ).text()
	if(ecom="E-commerce"){
	$('.trigger').html('E-commerce');
	}
}
else if(path=="#mobile-apps"){
$(".all").hide();		
$(".mobileapps").show();
var ecom = $( "div.trigger" ).text()
	if(ecom="Apps"){
	$('.trigger').html('Apps');
	}
}	
else if(path=="#applications"){
$(".all").hide();		
$(".applications").show();
var ecom = $( "div.trigger" ).text()
	if(ecom="Web Applications"){
	$('.trigger').html('Web Applications');
	}
}
});
</script>